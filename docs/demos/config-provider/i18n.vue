<template>
  <div class="app">
    <header class="app-header">
      <h1>GenUI Chat - 国际化示例</h1>
      <div class="header-actions">
        <select v-model="currentLocale" class="locale-select">
          <option value="zh_CN">简体中文</option>
          <option value="en_US">English</option>
        </select>
      </div>
    </header>

    <GenuiConfigProvider :locale="currentLocale" :i18n="customI18n" id="i18n-chat">
      <GenuiChat :url="url" model="deepseek-v3.2" :messages="messages" />
    </GenuiConfigProvider>
  </div>
</template>

<script setup lang="ts">
import { ref, onBeforeUnmount } from 'vue';
import { GenuiConfigProvider, GenuiChat, useI18n } from '@opentiny/genui-sdk-vue';
import type { I18nMessages } from '@opentiny/genui-sdk-vue';

const url = 'https://your-chat-backend/api';

const currentLocale = ref('en_US');

// 获取全局 i18n 实例
const i18n = useI18n();

// 在组件挂载前保存原始消息的深拷贝（在 ConfigProvider 应用自定义消息之前）
const originalMessages = JSON.parse(JSON.stringify(i18n.messages)) as I18nMessages;

// 自定义国际化消息
const customI18n: I18nMessages = {
  zh_CN: {
    placeholder: {
      input: '请输入您的问题（已自定义）',
    },
    footer: {
      aiGenerated: '内容由AI生成，仅供参考（已自定义）',
    },
  },
  en_US: {
    placeholder: {
      input: 'Please enter your question (customized)',
    },
    footer: {
      aiGenerated: 'Content generated by AI, for reference only (customized)',
    },
  },
};

// 组件卸载时还原原始消息
onBeforeUnmount(() => {
  // 使用 isCoverSameKey=true 完全替换修改过的语言对象，还原到原始状态
  i18n.mergeMessages(originalMessages, true);
  // 还原语言环境到默认值
  i18n.setLocale('zh_CN');
});

const messages = [
  {
    role: 'user',
    content: 'Hello, how are you?',
  },
  {
    role: 'assistant',
    content: 'I am doing well, thank you for asking!',
  },
];
</script>

